async function seed() {
  console.log("🌷 TulipDay seed gestart...\n");

  // Locaties
  console.log(`📍 ${locations.length} locaties invoegen...`);
  for (const loc of locations) {
    const { error } = await sb.from("locations").upsert(loc, { onConflict: "slug" });
    if (error) {
      console.error(`  ✗ ${loc.title}: ${error.message}`);
    } else {
      console.log(`  ✓ ${loc.title}`);
    }
  }

  // Routes
  console.log(`\n🗺  ${routes.length} routes invoegen...`);
  for (const route of routes) {
    const { error } = await sb.from("routes").upsert(route, { onConflict: "slug" });
    if (error) {
      console.error(`  ✗ ${route.title}: ${error.message}`);
    } else {
      console.log(`  ✓ ${route.title}`);
    }
  }

  console.log("\n✅ Seed klaar!");
}

